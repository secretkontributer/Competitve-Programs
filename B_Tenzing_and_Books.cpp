void solve()
{
    ll n;cin>>n;
    ll x;cin>>x;
    int arr[32];
    for(int i=0;i<32;i++)
    {
        arr[i]=x%2;
        x=x/2;
    }
    int a[n];int b[n];int c[n];
    for(int i=0;i<n;i++)cin>>a[i];
    for(int i=0;i<n;i++)cin>>b[i];
    for(int i=0;i<n;i++)cin>>c[i];
    int ans[32];for(int i=0;i<n;i++)ans[i]=0;
    int cnta=0;
    int cntb=0;
    int cntc=0;
    int pa=0;
    int pb=0;
    int pc=0;
    int glb=0;
    int pp=3*n;
    while(pp--)
    {
        int temp1[32];
        if(pa==0)
        {
        if(cnta!=n)
        {
        for(int i=0;i<32;i++)
        {
            temp1[i]=a[cnta]%2;
            a[cnta]=a[cnta]/2;
        }
        }else
        {
            pa=1;
        }
        }
        int temp2[32];
        if(pb==0)
        {
         if(cntb!=n)
         {
        for (int i = 0; i < 32; i++)
        {
            temp2[i] = b[cntb] % 2;
            b[cntb] = b[cntb] / 2;
        }
         }else
         {
            pb=1;
         }
        }
        int temp3[32];
        if(pc==0)
        {
            if(cntc!=n)
            {
        for (int i = 0; i < 32; i++)
        {
            temp3[i] = c[cntc] % 2;
            c[cntc] = c[cntc] / 2;
        }
            }else
            {
                pc=1;
            }
        }
        int flag1=0,flag2=0,flag3=0;
        if(pa==0)
        {
        for(int i=0;i<32;i++)
        {
            if(arr[i]==0&&temp1[i]==1)
            {
                flag1=1;
                pa=1;
                break;
            }
        }
        if(flag1==0)
        {
            for (int i = 0; i < 32; i++)
            {
                if(temp1[i]==1)
                ans[i]=temp1[i];
            }
            cnta++;
        }
        }
        if(pb==0)
        {
        for (int i = 0; i < 32; i++)
        {
            if (arr[i] == 0 && temp2[i] == 1)
            {
                flag2 = 1;
                pb=1;
                break;
            }
        }
        if (flag2 == 0)
        {
            for (int i = 0; i < 32; i++)
            {
                if (temp2[i] == 1)
                ans[i] = temp2[i];
            }
            cntb++;
        }
        }
        if(pc==0)
        {
        for (int i = 0; i < 32; i++)
        {
            if (arr[i] == 0 && temp3[i] == 1)
            {
                flag3 = 1;
                pc=1;
                break;
            }
        }
        if (flag3 == 0)
        {
            for (int i = 0; i < 32; i++)
            {
                if (temp3[i] == 1)
                ans[i] = temp3[i];
            }
            cntc++;
        }
        }
        int flag=0;
        for(int i=0;i<32;i++)
        {
            if(ans[i]!=arr[i])
            {
                flag=1;
                break;
            }
        }
        if(flag==0)
        {
            glb=1;
            break;
        }
        if(pa==0||pb==0||pc==0)
        {
            break;
        }
    }
    if(glb==1)
    {
        cout<<"Yes"<<"\n";
    }else
    cout<<"No"<<"\n";

}
