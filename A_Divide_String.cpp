void solve()
{
  int n;cin>>n;
  string str;cin>>str;
  char curr =str[0];
  int flag=0;
  for(int i=1;i<n;i++)
  {
    if(str[i]>curr)
    {
      flag=1;
      break;
    }
  }
  if(flag==1)
  {
    cout<<"Yes"<<"\n";
  }else
  {
    int h=-1;
    for(int i=1;i<n;i++)
    {
      if(str[i]==curr)
      {
        h=i;
        break;
      }
    }
    if((n-h)>h&&h!=-1)
    {
      cout<<"Yes"<<"\n";
    }else if((n-h)==h)
    {
      for(int i=1;i<h;i++)
      {
        if(str[i]>str[i+h])
        {
          flag=1;
          break;
        }
        if(str[i]<str[i+h])
        {
          flag=0;
          break;
        }
      }
      if(flag==1)
      {
        cout<<"No"<<"\n";
      }else
      cout<<"Yes"<<"\n";

    }else
    cout<<"No"<<"\n";
  }
}
