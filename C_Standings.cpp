bool comparator(pair<long double,ll> a,pair<long double,ll> b)
{
  if(a.first==b.first)
  {
    return a.second<b.second;
  }else {
    return a.first>b.first;
  }
  
}
void solve()
{
  ll n;cin>>n;
  pair<long double,ll> arr[n];
  for(int i=0;i<n;i++)
  {
    long double a;cin>>a;
    long double b;cin>>b;
    long double ans;
    ans=a/(a+b);
    arr[i].first=ans;
    arr[i].second=i;
  }
  sort(arr,arr+n,comparator);
  for(int i=0;i<n;i++)
  {
    cout<<arr[i].second+1<<" ";
  }
  cout<<"\n";
}
